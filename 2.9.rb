#2.9.1 10進数以外の整数リテラル

#2進数
0b11111111#=>255
#8進数
0337 #=>255
#16進数
0xff #=>255

#2.9.2 ビット演算
(~0b1010).to_s(2) #=>"-1011"
(0b1010 & 0b1100).to_s(2) #=>"1000"
(0b1010 | 0b1100).to_s(2) #=>"1110"
(0b1010^0b1100).to_s(2) #=>"110"
(0b1010>>1).to_s(2) #=>"101"
(0b1010<<1).to_s(2) #=>"10100"

